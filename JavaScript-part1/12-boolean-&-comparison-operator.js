// booleans & comparison operator

// booleans 
// true, false 

let num1 = 5;
let num2 = 6;

console.log(num1 <= num2);

// == vs ===

num1 = "8";
num2 = 8;
console.log(num1 == num2);  // true
console.log(num1 === num2);  // false

// != vs !==

console.log(num1 != num2);  // false
console.log(num1 !== num2);  // true
